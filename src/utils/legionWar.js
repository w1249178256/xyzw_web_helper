/**
 * 创建图
 */
export class HexGraph{
  //单例模式
  static instance;
  //存储节点,key:x_y,value对应相应的值
  nodes = {};

  //canvas偏移
  evenQDirs = [{q:-1,r:0}, {q:-1,r:-1}, {q:0,r:1}, {q:0,r:-1}, {q:1,r:0}, {q:1,r:-1}];
  oddQDirs = [{q:-1,r:0}, {q:-1,r:1}, {q:0,r:1}, {q:0,r:-1}, {q:1,r:0}, {q:1,r:1}];


  constructor(){
    if(HexGraph.instance) throw new Error('请使用HexGraph.getInstance()获取单例');
    this.loadStaticNodes();
    HexGraph.instance = this;
  }

  static getInstance(){
    if(!HexGraph.instance) return new HexGraph();
    return HexGraph.instance;
  }

  loadStaticNodes(){
    roadPointList.forEach((item) => {
      this.addNode(item); 
    });
  }

  /**
   * 删除所有节点,并导入静态路径节点
   */
  removeAllNode(){
    this.nodes={};
    this.loadStaticNodes();
  }

  /**
   * 添加节点
   * @param {Object} item 
   * @param {Object} item.id 坐标点,由x_y构成
   * @param {Object} item.type 坐标类型
   * @param {Object} item.belongsLegionId  所属俱乐部id
   * @param {Object} item.hP  血量
   * @param {Object} item.maxHP  最大血量
   * @param {Object} item.point 分数
   */
  addNode(item){
    const nodeId = item.id;
    if (this.nodes[nodeId]) return; // 避免重复添加
    // 节点结构：包含坐标、类型、颜色、邻接节点列表
    this.nodes[nodeId] = {
      id: item.id,
      type: item.type,
      belongsLegionId: item.belongsLegionId,
      hP: item.hP,
      maxHP: item.maxHP,
      point: item.point,
      colorBg: item?.belongsLegionInfo?.color||typeBg(item.type),
      typeName:typeName(item.type),
      position:{"x":(item.id+"").split("_")[0],"y":(item.id+"").split("_")[1]},
      neighbors: [] // 邻接节点（后续自动维护）
    };
    // 添加节点后，自动构建它与周围节点的邻接关系
    this.buildAdjacency(this.nodes[nodeId]);
  }

  /**
   * 传入数组对象
   */
  addNodeList(arrObject){
    arrObject.forEach(item=>{
      this.addNode(item);
    })
  }

  /**
   * 获取临界节点的关联信息
   * @param {*} node 
   */
  buildAdjacency(node){
    const { x ,y } = node.position;
    // 根据行的奇偶，获取对应的邻接方向
    const dirs = x % 2 === 0 ? this.evenQDirs : this.oddQDirs;
    if(x=="31"||x=="30"||x=="32"){
      if(y=="8"||y=="9"){
        // console.log(node,dirs)
      }
    }
    // 遍历6个邻接方向，检查是否存在节点
    dirs.forEach(({ q: dq, r: dr }) => {
      const neighborId = `${parseInt(x) + dq}_${parseInt(y) + dr}`;
      const neighborNode = this.nodes[neighborId];
      // 如果邻接节点存在，互相添加到neighbors
      if (neighborNode) {
        if (!node.neighbors.includes(neighborNode)) node.neighbors.push(neighborNode);
        if (!neighborNode.neighbors.includes(node)) neighborNode.neighbors.push(node);
      }
    });
  }

  /**
   * 工具方法：根据坐标获取节点（寻路时用）
   * @param {*} nodeStr x_y 坐标字符串
   * @returns 
   */
  getNodeByCoords(nodeStr) {
    return this.nodes[nodeStr];
  }

  // 工具方法：获取所有节点（绘制canvas时用）
  getAllNodes = function(){
    return Object.values(this.nodes);
  }
  /**
   * 找到两个结点之间的最短路径并将路径上的结点返回
   * @param {*} start 开始结点
   * @param {*} end 终点
   * @param {*} legionId 经过的据点需要是该legionid
   * @returns false 表示无效果,成功返回一个包含start到end结点的结点数组
   */
  findShortestPath(start, end,legionId) {
    //判断是否包含_的字符串
    if(!start.includes("_")||!start.includes("_")||start===end){
      return false;
    }
    //确保nodes中有起始结点和终点
    let startNode = this.getNodeByCoords(start)
    let endNode = this.getNodeByCoords(end)
    if(!startNode||!endNode){
      return false;
    }

    // BFS核心：队列（存储待遍历节点） + 前驱映射（记录路径回溯关系）
    const queue = [startNode];
    const predecessors = new Map(); // key: 节点id，value: 前驱节点（用来回溯路径）
    predecessors.set(startNode.id, null); // 起点无前驱

    // 4. 开始BFS遍历
    let found = false;
    while (queue.length > 0 && !found) {
      const currentNode = queue.shift(); // BFS是队列（先进先出）
      if(currentNode.type!=9&&currentNode.belongsLegionId!=legionId){
        continue;
      }
      // 遍历当前节点的所有邻接节点
      for (const neighbor of currentNode.neighbors) {
        if (!predecessors.has(neighbor.id)) { // 未访问过的节点
          predecessors.set(neighbor.id, currentNode); // 记录前驱
          queue.push(neighbor);

          // 找到终点，终止遍历
          if (neighbor.id === endNode.id) {
            found = true;
            break;
          }
        }
      }
    }
    //未找到路径
    if (!found) {
      return false;
    }
    // 从终点串到起点
    const path = [];
    let current = endNode;
    while (current) {
      path.push(current);
      current = predecessors.get(current.id);
    }
    // 反转后：起点 → 终点
    path.reverse(); 
    return path;
    // if (!startNode) return { path: [], pathCoords: [], length: 0, error: "起始节点不存在" };
    // if (!endNode) return { path: [], pathCoords: [], length: 0, error: "终点节点不存在" };
  }
}

